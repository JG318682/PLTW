public class Movie
{
       private int rating;
       private String title; 

    public Movie()
    {  
        title = "";
    }
    
    public String getTitle() 
    {
    return title; 
    }
    public void setTitle(String t) 
    {
    title = t;
    }
    
    public int getRating() {
    return rating;
    }
    public void setRating(int r) 
    {
    rating = r;
    }

}
